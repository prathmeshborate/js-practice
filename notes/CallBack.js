//When you take a function and pass it into another function it is callback function

//It's a function that is passed as an argument to another function and is intended to be "called back" at a later time

/*
Callbacks are used to ensure that a certain piece of code doesn't execute until another piece of code has already finished execution. This is particularly useful in operations like:

Asynchronous operations: Making network requests, reading files, or handling timers without blocking the main thread.

Event handling: Actions performed in response to user interactions like clicks, keypresses, or mouse movements.
*/

/*
How Callbacks Work
When you pass a function as an argument to another function, you essentially give the receiving function the responsibility to execute that function at a specific time.
*/